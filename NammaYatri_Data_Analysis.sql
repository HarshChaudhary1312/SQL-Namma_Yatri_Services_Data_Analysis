# Most used payment menthod
select 
	faremethod,count(faremethod) as pay_fare
from trips
group by faremethod
order by pay_fare desc ;
-- ---------OR-------------------------------
select pay.method from payment pay
inner join
(select faremethod,count(distinct tripid) as paycnt
from trips
group by faremethod
order by paycnt desc limit 1) mostused
on pay.id=mostused.faremethod;



# The highest payment was made through which instrument
select * from trips
order by fare desc limit 1;
-- -----------OR------------------------
select a.method from payment a
inner join
(select * from trips
order by fare desc limit 1) b
on a.id=b.faremethod;



# Which two locations had the most trips
select loc_from,loc_to, count(tripid) 
from trips
group by loc_from,loc_to
order by count(tripid) desc limit 5;


# Top 5 earning drivers
select driverid, sum(fare) from trips
group by driverid
order by sum(fare) desc limit 5;
-- ----------OR---------------------------
select * from
(select *, dense_rank() over (order by fare desc) rnk
from
(select driverid, sum(fare) from trips
group by driverid)b)c
where rnk<6
;


# Which duration had more trips
select duration, count(distinct tripid) cnt 
from trips
group by duration
order by cnt desc;


# Which driver,custome pair had more orders
select driverid, custid, count(distinct tripid) as cnt
from trips
group by driverid, custid
order by cnt;
-- -------------------------------
select * from
(select *, rank() over(order by cnt desc) rnk from
(select driverid, custid, count(distinct tripid) cnt from trips
group by driverid, custid)c)d
where rnk=1;


# Search to estimate rate
select sum(searches_got_estimate)*100.0/sum(searches)as Estimate_to_search
from trips_details4;


# Which area got highest trips in which duration
select * from
(select *,rank() over(order by cnt desc) rnk from
(select duration,loc_from,count(distinct tripid) cnt from trips
group by duration,loc_from;)c)d
where rnk>1;


# Which area got the highest fares, cancellation,tripes
select loc_from,sum(fare) as far_e from trips
group by loc_from
order by far_e desc;
-- --------------OR------------------------
select * from
(select *, rank() over (order by fare desc) rnk
from
(select loc_from,sum(fare) as fare from trips
group by loc_from)b)c
where rnk=1;


